/*Animation frise1*/
function montrerFrise1() {
    return anime({
        targets: '.frise__legende1',
        translateY  : -100,
        autoplay: false,
        duration: 300,
        opacity: 1,
        easing: 'easeInOutSine'
    }).play;
}

function cacherFrise1() {
    return anime({
        targets: '.frise__legende1',
        translateY: 0,
        autoplay: false,
        duration: 300,
        opacity: 0,
        easing: 'easeInOutSine'
    }).play;
}

